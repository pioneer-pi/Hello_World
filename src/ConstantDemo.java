public class ConstantDemo {
    /*
        补充两个快捷方式:
            main方法的快捷键: psvm
            输出预计的快捷键：sout
        常量:
            概述：
                程序运行过程中，数值不会发生改变
            字面值常量:
                1.字符串常量,用""括起来
                2.字符常量,用''括起来
                3.整数常量
                4.小数常量
                5.布尔常量
                6.空常量
     */
    public static void main(String[] args) {

        System.out.println("abc");
        System.out.println("123");

        System.out.println('a');

        System.out.println(123);

        System.out.println(1.2);
        System.out.println(-1.2);

        System.out.println(true);
        System.out.println(false);

    }
}