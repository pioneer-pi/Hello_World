package cn.itcast.demo;

public class OperatorDemo5 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 10;

        System.out.println(a == b);
        System.out.println(a == c);
        System.out.println(a == b);
        System.out.println(b = a);
    }
}
