package cn.itcast.demo;

public class OperatorDemo1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println((float)a / (float)b);
        System.out.println(a % b);

        System.out.println(5/4);
        System.out.println(5.0/3);
    }
}
